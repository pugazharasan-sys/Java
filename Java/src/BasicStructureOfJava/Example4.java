package BasicStructureOfJava;

public class Example4 {
	
	public static void main(String[] args) {
		
		// println statement is used to print the data and a new line
		System.out.println("printed using println statement");
		
		// We can use println statement without passing any data it will just print a new line
		System.out.println();
		
		// print statement is only used to print the data
		System.out.print("printed using print statement");
		
		//we cannot use print statement without passing any data if we use then we will get compile time error
		//System.out.print(); CTE
	}

}
